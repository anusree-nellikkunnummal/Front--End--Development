function celcius(temp) {
  return (temp - 32) * (5 / 9);
}

function farenheit(temp) {
  return (temp * 9) / 5 + 32;
}
let tofaren = document.getElementById("f");
let tocelcius = document.getElementById("c");
let temp;
function c(temp) {
  if (tofaren.checked) {
    temp = document.getElementById("temphere").value;
    temp = Number(temp);
    temp = farenheit(temp);
    document.getElementById("l").innerHTML = temp;
  } else if (tocelcius.checked) {
    temp = document.getElementById("temphere").value;
    temp = Number(temp);
    temp = celcius(temp);
    document.getElementById("l").innerHTML = temp;
  } else {
    document.getElementById("l").innerHTML = "Enter a unit";
  }
}
